#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t	c;

	c = 0;
	if (s == NULL || f == NULL)
		return ;
	while (s[c] != '\0')
	{
		(*f)(&s[c]);
		c++;
	}
}
